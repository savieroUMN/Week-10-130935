package week10.saviero.id.ac.umn.Typecasting;

public class Typecasting {

	public static void main(String[] args) {
		int a = 100;
		long b = a;
		
		System.out.println(a);
		System.out.println(b);
		
		double x = 5.67;
		int y = (int) x;
		
		System.out.println(x);
		System.out.println(y);
		
	}

}
